package com.beike.util.lucene;

public class SearchStyle {

	private String search_action;
	private String current_search_goods;
	private String current_search_coupon;
	private String current_search_brand;

	public String getSearch_action() {
		return search_action;
	}

	public void setSearch_action(String search_action) {
		this.search_action = search_action;
	}

	public String getCurrent_search_goods() {
		return current_search_goods;
	}

	public void setCurrent_search_goods(String current_search_goods) {
		this.current_search_goods = current_search_goods;
	}

	public String getCurrent_search_coupon() {
		return current_search_coupon;
	}

	public void setCurrent_search_coupon(String current_search_coupon) {
		this.current_search_coupon = current_search_coupon;
	}

	public String getCurrent_search_brand() {
		return current_search_brand;
	}

	public void setCurrent_search_brand(String current_search_brand) {
		this.current_search_brand = current_search_brand;
	}

}
